def calculate_FCS(packetInfo, packetLength, payload):
    # Calculate the FCS
    aux = 0xFF
    fcs = (packetInfo + sum(packetLength) + sum(payload)) & aux
    return hex(fcs)

# Ping
print(calculate_FCS(0x40, [0x00, 0x00], [0x00]))
# Start
print(calculate_FCS(0x41, [0x00, 0x00], [0x00]))
# Stop
print(calculate_FCS(0x42, [0x00, 0x00], [0x00]))
# Pause
print(calculate_FCS(0x43, [0x00, 0x00], [0x00]))
# Resume
print(calculate_FCS(0x44, [0x00, 0x00], [0x00]))
# CFG FREQ
print(calculate_FCS(0x45, [0x04, 0x00], [0x92, 0x09, 0x00, 0x00]))
# CFG PHY
print(calculate_FCS(0x47, [0x01, 0x00], [0x11]))
